import turtle
t = turtle.Turtle()
color_tree = "green"
color_roof = "white"
def rectangular(width, length):
    t.pendown()
    for x in range(2):
        t.fd(width)
        t.lt(90)
        t.fd(length)
        t.lt(90)
    t.penup()
def triangle(a,b, color):
    t.pendown()
    t.fillcolor(color)
    t.begin_fill()
    t.lt(30)
    t.fd(a)
    t.rt(60)
    t.fd(a)
    t.rt(150)
    t.fd(b)
    t.rt(180)
    t.end_fill()
    t.penup()
rectangular(300,140)
t.goto(110,0)
rectangular(40,100)
t.goto(150,0)
rectangular(40,100)
t.goto(140,175)
rectangular(20,40)
t.goto(0,140)
triangle(130,3**0.5*130, color_roof)
t.goto(-80,-100)
rectangular(20,70)
t.goto(-139,-30)
triangle(80,3**0.5*80, color_tree)
t.goto(-122,10)
triangle(60,3**0.5*60, color_tree)
t.goto(-105,40)
triangle(40,3**0.5*40, color_tree)
turtle.done()